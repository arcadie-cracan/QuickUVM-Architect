  /** Scoreboard from the agents' streams (`analysis.scoreboards`, docs/03). */
  async addScoreboard(preselSource?: string, cfg: TbEditTarget = this.config): Promise<void> {
    if (!cfg.configUri) {
      return this.warnNoConfig();
    }
    // docs/07 P3c — on a composition the endpoints also include the children's agents
    // (`<subenv>.<agent>`), which is what makes a scoreboard cross-block. `childAgents`
    // is only tracked on the active config; a per-file editor target keeps its own.
    const agents = scoreboardEndpoints(
      this.agentNames(cfg),
      cfg === this.config ? this.config.childAgents : {}
    );
    if (!agents.length) {
      return this.warnNoAgents();
    }
    let source =
      preselSource && agents.includes(preselSource) ? preselSource : undefined;
    if (!source) {
      source = await vscode.window.showQuickPick(agents, {
        title: vscode.l10n.t("Scoreboard: stimulus/source agent"),
        placeHolder: vscode.l10n.t("the stream fed into the predictor"),
      });
      if (!source) {
        return;
      }
    }
    const src = source;
    const NONE = vscode.l10n.t("$(circle-slash) None (single-stream)");
    const monPick = await vscode.window.showQuickPick(
      [NONE, ...agents.filter((a) => a !== src)],
      {
        title: vscode.l10n.t("Scoreboard: response/monitor agent (A2 two-stream)"),
        placeHolder: vscode.l10n.t("the observed stream compared against the prediction"),
      }
    );
    if (monPick === undefined) {
      return;
    }
    const monitor = monPick === NONE ? undefined : monPick;

    let match: "in_order" | "out_of_order" = "in_order";
    let matchKey: string | undefined;
    if (monitor) {
      const IN = vscode.l10n.t("In order (FIFO pair)");
      const OOO = vscode.l10n.t("Out of order (keyed pool — a reordering DUT)");
      const mPick = await vscode.window.showQuickPick([IN, OOO], {
        title: vscode.l10n.t("Scoreboard: comparison strategy"),
      });
      if (!mPick) {
        return;
      }
      if (mPick === OOO) {
        match = "out_of_order";
        matchKey = await vscode.window.showInputBox({
          title: vscode.l10n.t("Match key (monitor transaction field)"),
          prompt: vscode.l10n.t("the field each response is keyed on (required for out-of-order)"),
          validateInput: (v) =>
            SV_IDENT_RE.test(v)
              ? undefined
              : vscode.l10n.t("invalid SystemVerilog identifier"),
        });
        if (!matchKey) {
          return;
        }
      }
    }

    const taken = new Set(
      (cfg.current.analysis?.scoreboards ?? [])
        .map((s) => s.name)
        .filter((n): n is string => Boolean(n))
    );
    const name = await vscode.window.showInputBox({
      title: vscode.l10n.t("Scoreboard name"),
      value: Actions.uniqueName("sbd", taken),
      validateInput: (v) =>
        !SV_IDENT_RE.test(v)
          ? vscode.l10n.t("invalid SystemVerilog identifier")
          : taken.has(v)
            ? vscode.l10n.t("a scoreboard with this name already exists")
            : undefined,
    });
    if (!name) {
      return;
    }
    if (
      await cfg.apply((t) =>
        ops.addScoreboard(t, { name, source: src, monitor, match, matchKey })
      )
    ) {
      this.log.appendLine(
        `[actions] addScoreboard ${name} (${src}${monitor ? ` vs ${monitor}` : ""})`
      );
    }
  }

