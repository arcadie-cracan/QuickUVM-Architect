// Node tests for the pure part of the source list (src/filelistops.ts):
//   npm run test:filelist
// The two real pitfalls (CLAUDE.md): quoting for slang (splits on
// spaces) and excluding the quick-uvm output folder (auto-poisoning
// the model with the DUT stub after the first "Generate testbench").
import assert from "node:assert/strict";
import { mkdtempSync } from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { pathToFileURL } from "node:url";
import esbuild from "esbuild";

const outDir = mkdtempSync(join(tmpdir(), "quickuvm-filelist-"));
const outFile = join(outDir, "filelistops.mjs");
await esbuild.build({
  entryPoints: ["src/filelistops.ts"],
  outfile: outFile,
  bundle: true,
  format: "esm",
  platform: "node",
  logLevel: "silent",
});
const { quoteFlistLine, renderFlist, outputDirExclude, resolveLocPath } =
  await import(pathToFileURL(outFile));

let passed = 0;
function test(name, fn) {
  fn();
  passed += 1;
  console.log(`  ok ${name}`);
}

test("cale cu spatii: citata integral (slang desparte pe spatii)", () => {
  assert.equal(
    quoteFlistLine("C:\\proj\\my design\\top.sv"),
    '"C:\\proj\\my design\\top.sv"'
  );
});

test("cale deja citata: neschimbata (fara dublare)", () => {
  assert.equal(quoteFlistLine('"C:/x/top.sv"'), '"C:/x/top.sv"');
});

test("+incdir+ cu spatii: se citeaza DOAR calea (+incdir+\"cale\")", () => {
  assert.equal(
    quoteFlistLine("+incdir+C:/cale cu spatii/include"),
    '+incdir+"C:/cale cu spatii/include"'
  );
});

test("+incdir+ deja citat: neschimbat", () => {
  assert.equal(quoteFlistLine('+incdir+"C:/x"'), '+incdir+"C:/x"');
});

test("+define+ si alte plusargs: neatinse (nu contin spatii)", () => {
  assert.equal(quoteFlistLine("+define+FOO=1"), "+define+FOO=1");
});

test("comentarii // si #: pastrate ca atare (doar trim)", () => {
  assert.equal(quoteFlistLine("  // generated by bender"), "// generated by bender");
  assert.equal(quoteFlistLine("# nota"), "# nota");
});

test("renderFlist: liniile goale dispar, restul citate, newline final", () => {
  const text = renderFlist([
    "a b.sv",
    "",
    "   ",
    "+incdir+inc dir",
    "// c",
  ]);
  assert.equal(text, '"a b.sv"\n+incdir+"inc dir"\n// c\n');
});

test("outputDir relativ: exclude <dir>/** (anti-otravire dupa generate)", () => {
  assert.equal(outputDirExclude("tb"), "tb/**");
});

test("outputDir cu backslash si separatori finali: normalizat", () => {
  assert.equal(outputDirExclude(" tb\\sub\\ "), "tb/sub/**");
  assert.equal(outputDirExclude("tb//"), "tb/**");
});

test("outputDir gol sau '.': nu se exclude nimic (nu radacina!)", () => {
  assert.equal(outputDirExclude(""), null);
  assert.equal(outputDirExclude("   "), null);
  assert.equal(outputDirExclude("."), null);
});

test("outputDir absolut: nu devine exclude relativ", () => {
  assert.equal(outputDirExclude("C:\\abs\\out"), null);
  assert.equal(outputDirExclude("/abs/out"), null);
});

// resolveLocPath uses the platform path (that's how openLoc consumes it); the
// Windows forms are checked ONLY on win32 — on posix, path.join does not understand "\\"
// (the test would otherwise fail on Linux, a real environment regression), so there
// the posix equivalents are checked.
if (process.platform === "win32") {
  test("resolveLocPath: loc.file relativizat de slang (..\\..) -> cale absoluta", () => {
    // pitfall 8: slang relativizes against cwd; the join normalizes ..
    assert.equal(
      resolveLocPath("C:\\ws\\proj", "..\\..\\other\\top.sv"),
      "C:\\other\\top.sv"
    );
    assert.equal(
      resolveLocPath("C:\\ws\\proj", "src/adder.sv"),
      "C:\\ws\\proj\\src\\adder.sv"
    );
  });

  test("resolveLocPath: loc.file deja absolut ramane neatins", () => {
    assert.equal(resolveLocPath("C:\\ws", "D:\\alt\\x.sv"), "D:\\alt\\x.sv");
  });
} else {
  test("resolveLocPath (posix): relativ -> join cu radacina, .. normalizat", () => {
    assert.equal(
      resolveLocPath("/ws/proj", "../../other/top.sv"),
      "/other/top.sv"
    );
    assert.equal(
      resolveLocPath("/ws/proj", "src/adder.sv"),
      "/ws/proj/src/adder.sv"
    );
  });

  test("resolveLocPath (posix): loc.file deja absolut ramane neatins", () => {
    assert.equal(resolveLocPath("/ws", "/alt/x.sv"), "/alt/x.sv");
  });
}

console.log(`\ntest-filelist: ${passed} teste au trecut.`);
