  /**
   * Deletes a component of the verification environment (slice 2), with a MODAL
   * confirmation. Scoreboard/coverage/vseq are leaves (no one references them). For
   * an AGENT, the confirmation summarizes the cascade (its coverage, the scoreboards with
   * source/monitor == agent, the vseq steps) — the `removeAgent` mutation executes it.
   */
  async deleteComponent(
    kind: string,
    name: string,
    cfg: TbEditTarget = this.config
  ): Promise<void> {
    if (!cfg.configUri) {
      return this.warnNoConfig();
    }
    if (!name) {
      return;
    }
    let label: string;
    let detail: string | undefined;
    let mutate: (t: string) => string;
    switch (kind) {
      case "scoreboard":
        label = vscode.l10n.t('scoreboard "{0}"', name);
        mutate = (t) => ops.removeScoreboard(t, name);
        break;
      case "coverage":
        label = vscode.l10n.t('the coverage collector for "{0}"', name);
        mutate = (t) => ops.removeCoverage(t, name);
        break;
      case "vseq":
        label = vscode.l10n.t('virtual sequence "{0}"', name);
        mutate = (t) => ops.removeVirtualSequence(t, name);
        break;
      case "probe":
        label = vscode.l10n.t('probe "{0}"', name);
        mutate = (t) => ops.removeProbe(t, name);
        break;
      case "agent": {
        label = vscode.l10n.t('agent "{0}"', name);
        const cascade = this.agentCascade(name, cfg);
        if (cascade.length) {
          detail = vscode.l10n.t("This also removes: {0}.", cascade.join(", "));
        }
        mutate = (t) => ops.removeAgent(t, name);
        break;
      }
      default:
        return;
    }
    const DELETE = vscode.l10n.t("Delete");
    const pick = await vscode.window.showWarningMessage(
      vscode.l10n.t("Delete {0}?", label),
      { modal: true, detail },
      DELETE
    );
    if (pick !== DELETE) {
      return;
    }
    if (await cfg.apply(mutate)) {
      this.log.appendLine(`[actions] deleteComponent ${kind} ${name}`);
    }
  }

  /** The summary of what falls when an agent is deleted, for the modal confirmation. */
  private agentCascade(name: string, target: TbEditTarget): string[] {
    const cfg = target.current;
    const out: string[] = [];
    if ((cfg.analysis?.coverage ?? []).some((c) => coveredAgent(c) === name)) {
      out.push(vscode.l10n.t("its coverage collector"));
    }
    for (const s of cfg.analysis?.scoreboards ?? []) {
      if (s.source === name || s.monitor === name) {
        out.push(vscode.l10n.t('scoreboard "{0}"', s.name ?? "?"));
      }
    }
    for (const v of cfg.virtual_sequences ?? []) {
      const body = v.body ?? [];
      if (!body.some((st) => st.agent === name)) {
        continue;
      }
      // the sequence disappears only if ALL steps belong to the agent
      out.push(
        body.every((st) => st.agent === name)
          ? vscode.l10n.t('virtual sequence "{0}"', v.name ?? "?")
          : vscode.l10n.t('a step in "{0}"', v.name ?? "?")
      );
    }
    return out;
  }

